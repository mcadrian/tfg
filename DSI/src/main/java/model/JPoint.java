package model;

public class JPoint {

    private int position;
    private int cycle;
    
	public JPoint(int position, int cycle) {
		this.position = position;
		this.cycle = cycle;
	}

	public int getPosition() {
		return position;
	}

	public void setPosition(int position) {
		this.position = position;
	}

	public int getCycle() {
		return cycle;
	}

	public void setCycle(int cycle) {
		this.cycle = cycle;
	}

	
    
    
}
